let a = 5;

console.log("Initial a:", a);

console.log("a++:", a++);  
console.log("After post increment a++:", a); 

console.log("++a:", ++a);  
console.log("After pre increment ++a:", a); 

console.log("a--:", a--);  
console.log("After post decrement a--:", a); 

console.log("--a:", --a); 
console.log("After pre decrement --a:", a);
